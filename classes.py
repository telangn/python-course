class User:
    def __init__(self, name, email, age):
        self.name = name
        self.email = email
        self.age = age

    def greeting(self):
        return f'My name is {self.name} and I am {self.age}'

    def has_birthday(self):
        self.age += 1

class Customer(User):
    def __init__(self, name, email, age):
        self.name = name
        self.email = email
        self.age = age
        self.balance = 0

    def set_balance(self, balance):
        self.balance = balance

    def greeting(self):
        return f'My name is {self.name} and I am {self.age} and my balance is {self.balance}'



ninad = User('ninad', 'test.com', 44)
janet = Customer('janet', 'test.com', 33)
janet.set_balance(500)
ninad.has_birthday()

print(janet.greeting())
print(ninad.name)
print(ninad.greeting())